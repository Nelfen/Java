package Chapter02;

public class BinaryTest {
	public static void main(String[] args) {
		int bNum = 0B1010;	//2진수
		int dNum = 10;		//10진수
		int oNum = 012;		//8진수
		int hNum = 0xA;		//16진수

		System.out.println(bNum);
		System.out.println(dNum);
		System.out.println(oNum);
		System.out.println(hNum);
	}
}